	<h2 align="center" style="color:#093555;font-weight: bold;font-family:Arial Black;font-size:30px;">About The Movement</h2>
	
	<br>
	<br>
	
	<p class="information" >The Red Cross idea was born in 1859, when Henry Dunant, a young Swiss 
	from Geneva, came upon the scene of the bloody battle of Solferino in Northern
	Italy, fight between the armies of imperial Austria and the Franco-Sardinian
	alliance, about 40,000 laid dead or dying on the battlefield and the wounded 
	were without medical attention.
	</p>
	
	<p class="information">
	Henry Dunant organized nearby people of Castiglione and Solferino-travellers,
	priests, housewives, available to provide the wounded with food and First Aid.
	In 1862 he published the book A Memory of Solferino, in which he described the
	horror of the fight and condition of the thousands of wounded left without proper
	care. Dunant called for the creation of national relief societies, an idea which
	eventually led to the formation of the Red Cross.
	</p>
	
	<p class="information">
	To help promote these ideas, four citizens of Geneva - General Henri Guillaume 
	Dufour, Dr. Louis Appia, Dr. Theodore Maunoir and Gustave Moymer- Joined Henry
	Dunant and set up first the International Committee for Relief to the Wounded,
	which was later renamed as the International Committee of the Red Cross (ICRC).
	In a conference held at Geneva where representatives from sixteen countries 
	attended in October 1863. They adopted ten resolutions that formed the founding
	charter of the Red Cross, defining functions and working methods for the relief
	to the wounded. The Red Cross was then a reality.

	</p>
	
	<p class="information">
	However, the Committee's main task still lay ahead: to persuade the governments
	that the wounded and those carrying them should be considered non-combatants,
	since they were not or were no longer taking part in the fighting and needed 
	protection. This concept of Neutrality has been embodied in an international 
	treaty providing for the protection of war victims, medical personnel, hospitals 
	and ambulances. In August 1864, the Swiss Government convened in Geneva a diplomatic
	conference, where the delegates of twelve participating governments adopted the Geneva
	Convention for the Amelioration of the Condition of the Wounded Armies in the field.
	</p>
	
	<p class="information">
	This treaty, with its ten articles, is a milestone in the history of Humanity.
	Until then, war and law had been largely considered irreconcilable. From then on
	the wounded were to "be collected and cared for" and ambulances, military hospitals
	and medical staff were to be "recognized as neutral and, as such, protected and respected
	by the belligerent, to whatever nation they belong". The Convention recognized the Red
	Cross on white ground- first adopted in 1863 as the symbol of the embryonic Red Cross
	Movement- as a protective emblem to be used by all armies for identifying medical personnel
	hospitals and ambulances.
	</p>
	
	
	
